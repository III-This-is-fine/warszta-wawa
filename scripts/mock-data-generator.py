def random_workshop():
    category = random_category()
    full_description = categories_descriptions[category]
    # print(list(full_description.items()))
    title, description = random.choice(list(full_description.items()))
    address = random.choice(list(addresses.keys()))
    district, (lat, lng) = addresses[address]
    assert district in districts
    return Workshop(
        title=title,
        type=category,
        place=address,
        coordinates=LatLng(lat, lng),
        district=district,
        period=random_period(),
        day=random_day(),
        age=random_age_range(),
        description=description,
        price=random_price(),
    )


